def generate_html(group_name: str, processors: List[Dict], connections: List[Dict]) -> str:
    """Generate a premium HTML visualization of the NiFi flow."""
    
    # Build Mermaid graph content
    nodes = []
    for proc in processors:
        comp = proc.get("component", {})
        p_name = comp.get("name")
        p_id = comp.get("id")
        # Clean name for Mermaid compatibility
        safe_name = p_name.replace('"', "'")
        nodes.append(f'    P{p_id}["{safe_name}"]')

    edges = []
    for conn in connections:
        comp = conn.get("component", {})
        src_id = comp.get("source", {}).get("id")
        dest_id = comp.get("destination", {}).get("id")
        rels = ", ".join(comp.get("selectedRelationships", []))
        edges.append(f'    P{src_id} -- "{rels}" --> P{dest_id}')

    mermaid_content = "graph TD\n" + "\n".join(nodes) + "\n" + "\n".join(edges)

