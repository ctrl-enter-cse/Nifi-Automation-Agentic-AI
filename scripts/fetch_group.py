async def main() -> None:
    parser = argparse.ArgumentParser(description="Fetch and visualize NiFi process groups.")
    parser.add_argument("name", nargs="+", help="Name of the process group to fetch")
    parser.add_argument("--visual", action="store_true", help="Generate a flow_viz.html visualization file")
    args = parser.parse_args()
    
    name = " ".join(args.name)
    
    try:
        group = await find_group_by_name(name)
        if not group:
            print(f"Group '{name}' not found.")
            return

        group_id = group["id"]
        print(f"--- Group Details ---")
        print(f"Name: {name}")
        print(f"ID: {group_id}")
        
        # 1. Fetch Processors
        processors_data = await _request("GET", f"/process-groups/{group_id}/processors")
        processors_list = processors_data.get("processors", [])
        
        print(f"\n--- Processors inside the group ---")
        processor_map = {}
        for proc in processors_list:
            comp = proc.get("component", {})
            p_id = comp.get('id')
            p_name = comp.get('name')
            processor_map[p_id] = p_name
            print(f"[{p_name}] | ID: {p_id} | Type: {comp.get('type')}")

        # 2. Fetch Connections (The Flow)
        connections_data = await get_connections(group_id)
        connections_list = connections_data.get("connections", [])
        
        print(f"\n--- Flow Relationships (Connections) ---")
        for conn in connections_list:
            comp = conn.get("component", {})
            src = comp.get("source", {}).get("name")
            dest = comp.get("destination", {}).get("name")
            relationships = ", ".join(comp.get("selectedRelationships", []))
            print(f"[{src}]  --({relationships})-->  [{dest}]")
            
        # 3. Handle Visualization
        if args.visual:
            print("\nGenerating HTML visualization...")
            html_content = generate_html(name, processors_list, connections_list)
            with open("flow_viz.html", "w", encoding="utf-8") as f:
                f.write(html_content)
            print("Successfully created 'flow_viz.html'. You can open this file in your browser.")
        
    except Exception as exc:
        print("FAILED:", exc)
        sys.exit(1)

if __name__ == "__main__":
    asyncio.run(main())
